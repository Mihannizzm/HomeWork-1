package geometricFigures;

public class Triangle {

    //Метод для вычисления площади треугольника зная длину стороны и высоту.
    public static double squareOfTriangle(double sideOfTriangle, double heightOfTriangle) {
        double squareOfTriangle = (sideOfTriangle * heightOfTriangle) / 2;
        return squareOfTriangle;

    }
}
